# There may be times when we want to specify a type on to a variable and this can be done with casting. It simply means to convert a data type to another according to need.
# In the example given we are going to change data type from one to another.

# With the help of casting we can perform various types of operations.

x = int(1)  #From integer to integer.
y = int(1.1) #From float to integer.
z = int("1")  #From string to integer.

print(type(x))
print(type(y))
print(type(z))
print("\n")

x = str(1)  #From integer to string..
y = str(1.1) #From float to string.
z = str("1")  #From string to string..

print(type(x))
print(type(y))
print(type(z))
print("\n")

x = float(1)  #From integer to float.
y = float(1.1) #From float to float.
z = float("1")  #From string to float.

print(type(x))
print(type(y))
print(type(z))
